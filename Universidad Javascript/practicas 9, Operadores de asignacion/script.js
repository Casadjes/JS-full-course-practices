let a = 1;

//operador de asignacion compuesto

a += 3; // a = a + 3;
console.log(a);

a -= 2; // a = a - 2;
console.log(a);

/* 
operadores

*=
/=
%=
**=

*/

a *= 5; // a = a * 5;
console.log(a);

a /= 2; // a = a / 2;
console.log(a);

a %= 2; // a = a % 2;
console.log(a);

a **= 5; // a = a ** 5;
console.log(a);
