let a = 3;
let b = 2;

//suma - addition
let z = a + b;
console.log("Resultado de la suma: " + z);

//resta - subtraction
z = a - b;
console.log("Resultado de la resta: " + z);

//multiplicacion - multiplication
z = a * b;
console.log("Resultado de la multiplicacion: " + z);

//division
z = a / b;
console.log("Resultado de la division: " + z);

//modulo (residuo) - modulus (remainder)
z = a % b;
console.log("Resultado de operacion modulo (residuo): " + z);

//exponente - exponentiation
z = a ** b;
console.log("Resultado del operador exponente: " + z);
