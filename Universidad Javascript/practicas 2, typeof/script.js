/* Las variables en javascript son dinamicas,
lo que quiere decir que le asignamos un valor y
se lo podemos cambiar en cualquier momento */

//string
var nombre = "jesus";
console.log(nombre);

nombre = 10;
console.log(typeof nombre);

//number

var number = 1000;
console.log(typeof number);

//object

var object = {
  nombre: "jesus",
  apellido: "casado",
  telefono: 1011001000,
};

console.log(typeof object);
